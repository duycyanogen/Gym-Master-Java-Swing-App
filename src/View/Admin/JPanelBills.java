    private void txtSearchInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchInputKeyReleased
        // TODO add your handling code here:
        DefaultTableModel table = (DefaultTableModel) tableHoaDon.getModel();
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<DefaultTableModel>(table);
        String search = txtSearchInput.getText();
        if (search.trim().length() == 0) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + search));
        }
        tableHoaDon.setRowSorter(rowSorter);
    }//GEN-LAST:event_txtSearchInputKeyReleased

    public void showAllDataHD() {
        lhd = hdc.getData();
        int stt = 0;
        float TC = 0f;
        if (tableHoaDon.getRowCount() > 0) {
            dTable.setRowCount(0); // reset nd trong bang ve 0
        }
        ArrayList<HoaDon> listHD = new ArrayList<>();
        listHD = hdc.getData();
        NhanVienCTR nvc = new NhanVienCTR();
        for (HoaDon hoaDon : listHD) {
            dTable.addRow(new Object[]{++stt, hoaDon.getMaHD(), hoaDon.getNgayLap(),
                hvctr.getData(hoaDon.getMaHV()).getTenHV(), hoaDon.getMaNV(),
                np.FloatToVND(hdc.getPriceByMaHD(hoaDon.getMaHD()))});
            TC += hdc.getPriceByMaHD(hoaDon.getMaHD());

        }

    }

