    private void tableHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableHoaDonMouseClicked
        // TODO add your handling code here:
        int rowSelected = tableHoaDon.getSelectedRow();
        if (rowSelected != -1) {
            int index = Integer.parseInt(tableHoaDon.getValueAt(rowSelected, 0) + "") - 1;
            MaHD = lhd.get(index).getMaHD();
            txtInvoiceNo.setText(MaHD + "");
            txtStaffID.setText(hdc.getDataByMaHD(MaHD).getMaNV() + "");
            txtInvoiceDate.setText(hdc.getDataByMaHD(MaHD).getNgayLap());
            txtCustomerID.setText(hdc.getDataByMaHD(MaHD).getMaHV() + "");
            txtCustomerName.setText(hvctr.getData(hdc.getDataByMaHD(MaHD).getMaHV()).getTenHV() + "");
            txtToTalDue.setText(np.FloatToVND(hdc.getPriceByMaHD(MaHD)));
            txtTel.setText(hvctr.getData(hdc.getDataByMaHD(MaHD).getMaHV()).getSDT() + "");
            listCTHD = cthdc.getDataByMaHD(MaHD);
            dTable1 = (DefaultTableModel) tableCTHD.getModel();
            showData();
        }


    }//GEN-LAST:event_tableHoaDonMouseClicked

    private void showData() {
        if (tableCTHD.getRowCount() > 0) {
            dTable1.setRowCount(0); // reset nd trong bang ve 0
        }
        for (ChiTietHoaDon c : listCTHD) {
            dTable1.addRow(new Object[]{gtc.getData(c.getMaGoiTap()).getTenGoiTap(), lgtc.getTenLoaiGoiTap(c.getMaLoaiGoiTap()), c.getNgayBatDau(), c.getNgayKetThuc(), c.getMaVoucher(), c.getGiaCuoi()});

        }
    }
